package academy.devdojo.maratonajava.introducao.aula07;

public class Aula07Arrays01 {
    public static void main(String[] args) {
        //variávle do tipo reference
        //Arrays o tipo de varíavel usado para declarar é sempre do tipo reference
        int [] idades = new int[3];
        idades[0] = 21;
        idades[1] = 15;
        idades[2] = 11;
        System.out.println(idades[0]);
    }
    /* Variavel tipo reference
     * Arrays são objetos
     * As posições dos arryas sempre são inicializadas com um valor padrão
     */
}
